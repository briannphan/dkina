async  function send(){

    var toValue=document.getElementById("customerId").value;
       var amount=document.getElementById("setDigitalKinaValue").value;
     document.getElementById("DisplayValue").innerHtml=amount;
     amount = ethers.utils.parseUnits(amount,2);
     await ConverterContract.transfer(toValue,amount);
    //await ConverterContract.transferToAccount(toValue,amount,1000);

}
async  function getBalance(){
    var ownerId=document.getElementById("ownerId").value;
    var value=await ConverterContract.balanceOf(ownerId);
    value = ethers.utils.formatUnits(value,2);
    document.getElementById("DisplayValue").innerHtml=value;
}